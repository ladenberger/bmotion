package de.bmotion.core.objects;

public class ErrorObject {

	private String error;

	public ErrorObject() {
	}

	public ErrorObject(String error) {
		super();
		this.error = error;
	}

	public String getError() {
		return error;
	}

	public void setError(String error) {
		this.error = error;
	}

}
